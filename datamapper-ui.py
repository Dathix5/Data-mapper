    # button to add a file to a marker
    def buttonAddFile(self):
        if not self.checkCoordinates():
            return
        max_range = 0.0002
        fname = QFileDialog.getOpenFileName()
        if len(fname[0]) == 0:
            self.updateMap("No file selected")
            return
        with open('markers.json') as f:
            markers = json.load(f)
            f.close()
        lat = float(self.lineEdit.text())
        long = float(self.lineEdit_2.text())
        success = False
        for marker in markers["markers"]:
            range = math.sqrt(pow(lat - float(marker[1]), 2)+pow(long - float(marker[2]), 2))
            if range < max_range:
                # delete previous file
                marker_tag = marker[0]
                for filename in os.listdir('files'):
                    if filename.startswith(marker_tag):
                        file_path = os.path.join('files', filename)
                        os.remove(file_path)
                # add new file
                src = fname[0]
                extension = os.path.splitext(fname[0])[1]
                dst = "files/" + marker[0] + extension
                shutil.copy(src, dst)
                success = True
        if success:
            self.updateMap("File added successfully")
        else:
            self.updateMap("No markers found near coordinates")

