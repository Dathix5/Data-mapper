    # button for adding a marker
    def buttonAddMarker(self):
        max_range = 0.0002
        if not self.checkCoordinates():
            return
        with open('markers.json') as f:
            markers = json.load(f)
            f.close()
        name = "Tag_" + str(randint(100000, 999999))
        duplicate_name = True
        while duplicate_name:
            duplicate_name = False
            for marker in markers["markers"]:
                if marker[0] == name:
                    duplicate_name = True
                range = math.sqrt(pow(float(self.lineEdit.text()) - float(marker[1]), 2) + pow(float(self.lineEdit_2.text()) - float(marker[2]), 2))
                if range < max_range:
                    self.updateMap("Marker already exists")
                    return
            if duplicate_name:
                name = "Tag_" + str(randint(100000, 999999))
        lat = float(self.lineEdit.text())
        long = float(self.lineEdit_2.text())
        marker = [name, lat, long]
        markers["markers"].append(marker)
        with open('markers.json', 'w') as outfile:
            json.dump(markers, outfile)
            outfile.close()
        self.updateMap("Saved marker successfully")

    # button for removing a marker
    def buttonRemoveMarker(self):
        if not self.checkCoordinates():
            return False
        max_range = 0.0002
        with open('markers.json') as f:
            markers = json.load(f)
            f.close()
        lat = float(self.lineEdit.text())
        long = float(self.lineEdit_2.text())
        success = False
        delete = []
        for marker in markers["markers"]:
            range = math.sqrt(pow(lat - float(marker[1]), 2)+pow(long - float(marker[2]), 2))
            if range < max_range:
                delete.append(marker)
                success = True
                for filename in glob.glob("files/" + marker[0] + "*"):
                    os.remove(filename)
        markers["markers"] = [x for x in markers["markers"] if x not in delete]
        with open('markers.json', 'w') as outfile:
            json.dump(markers, outfile)
            outfile.close()
        if success:
            self.updateMap("Removed marker successfully")
        else:
            self.updateMap("No markers found near coordinates")

