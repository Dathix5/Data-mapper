    # check for valid coordinates or address
    def checkCoordinates(self):
        latitude = self.lineEdit.text()
        longitude = self.lineEdit_2.text()
        # check if latitude contains letters
        if any(char.isalpha() for char in latitude):
            # user is trying to input an address
            self.lineEdit_2.clear()
            # Try to find matching coordinates for the address
            geolocator = Nominatim(user_agent="YourApp")
            try:
                location = geolocator.geocode(latitude)
                if location:
                    # swap latitude and longitude with the ones matching the address
                    self.lineEdit.setText(str(location.latitude))
                    self.lineEdit_2.setText(str(location.longitude))
                    return True
                else:
                    # no matching coordinates found for the address
                    self.updateMap("Address not found")
                    return False
            except Exception as e:
                # handle any errors that might occur during geocoding
                self.updateMap("Error finding address: " + str(e))
                return False
        else:
            # latitude does not contain letters
            try:
                float(latitude)
            except ValueError:
                self.updateMap("Invalid latitude")
                return False
            try:
                float(longitude)
            except ValueError:
                self.updateMap("Invalid longitude")
                return False

            return True

